def add(a, b):
    """
    Сложение двух чисел.

    :param a: Первое число
    :param b: Второе число
    :return: Сумма чисел
    """
    return a + b

def subtract(a, b):
    """
    Вычитание двух чисел.

    :param a: Уменьшаемое
    :param b: Вычитаемое
    :return: Разность чисел
    """
    return a - b
